#Operator Perbandingan
# < : True jika nilai kanan lebih besar dari kiri
# <=: True jika nilai kanan lebih besar dari atau sama dengan nilai di kiri
# > : True jika nilai kanan lebih kecil dari kiri
# >=: True jika nilai di kanan lebih kecil dari atau sama dengan di kiri
# == : True jika nilai di kiri dan kanan adalah setara
# != : True jika tidak setara



